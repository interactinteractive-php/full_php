<script type="text/javascript">
    $('.newdes_menu_wrapper').on('click', '.nav-link', function() {
        $('.newdes_menu_wrapper').find('.nav-item-selected').removeClass('nav-item-selected');
        $(this).addClass('nav-item-selected');
        window.location.href = $(this).attr('href');
    });
    
    $(document.body).on('change', '#newdes-file-upload', function(){
        var input = $(this)[0];
        if (input.files && input.files[0]) {
            var reader = new FileReader();
            reader.onload = function (e) {
                $('.newdes-preview-image').css('background-image', 'url("'+e.target.result+'")');
            }
            reader.readAsDataURL(input.files[0]);
        }
    });    
    
    $(document.body).on('click', '#newdes-delete-file-upload', function(){
        $('.newdes-preview-image').css('background-image', '');
        $('#newdes-file-upload').val('');
    });    
    
    $('.change-newdes-logo').on('click', function() {
        var $dialogName = "newdes-dialog-change-logo";
        if (!$("#" + $dialogName).length) {
          $('<div id="' + $dialogName + '"></div>').appendTo("body");
        }
        var $dialog = $("#" + $dialogName);        
        $dialog.empty().append('<div class="justify-content-center d-flex flex-column" style="gap:10px"><div style="align-self: center;" class="newdes-preview-image"></div>\n\
            <div class="d-flex justify-content-center"><label style="align-self: center;" title="Зураг сонгох" for="newdes-file-upload" class="custom-file-upload">'+
                '<i class="icon-cloud-upload"></i>'+
            '</label><label style="align-self: center;" title="Зураг устгах" id="newdes-delete-file-upload" class="custom-file-upload ml8">'+
                '<i class="icon-cross3"></i>'+
            '</label></div>'+
            '<input id="newdes-file-upload" type="file"/></div>'
        );
        $dialog.dialog({
          cache: false,
          resizable: true,
          bgiframe: true,
          autoOpen: false,
          title: "Logo",
          width: 400,
          height: "auto",
          modal: true,
          open: function () {
          },
          close: function () {
            $dialog.empty().dialog("destroy").remove();
          },
          buttons: [
            {
              text: plang.get("insert_btn"),
              class: "btn btn-sm green-meadow",
              click: function () {
              },
            },
            {
              text: plang.get("close_btn"),
              class: "btn btn-sm blue-madison",
              click: function () {
                $dialog.dialog("close");
              },
            },
          ],
        });
        $dialog.dialog("open");        
    });
</script>
    