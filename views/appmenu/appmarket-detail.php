<script type="text/javascript">
    $('.appmarket_menu_wrapper').on('click', '.nav-link', function() {
        $('.appmarket_menu_wrapper').find('.nav-item-selected').removeClass('nav-item-selected');
        $(this).addClass('nav-item-selected');
        window.location.href = $(this).attr('href');
    });
    
    $(document.body).on('change', '#appmarket-file-upload', function(){
        var input = $(this)[0];
        if (input.files && input.files[0]) {
            var reader = new FileReader();
            reader.onload = function (e) {
                $('.appmarket-preview-image').css('background-image', 'url("'+e.target.result+'")');
            }
            reader.readAsDataURL(input.files[0]);
        }
    });    
    
    $(document.body).on('click', '#appmarket-delete-file-upload', function(){
        $('.appmarket-preview-image').css('background-image', '');
        $('#appmarket-file-upload').val('');
    });    
    
    $('.change-appmarket-logo').on('click', function() {
        var $dialogName = "appmarket-dialog-change-logo";
        if (!$("#" + $dialogName).length) {
          $('<div id="' + $dialogName + '"></div>').appendTo("body");
        }
        var $dialog = $("#" + $dialogName);        
        $dialog.empty().append('<div class="justify-content-center d-flex flex-column" style="gap:10px"><div style="align-self: center;" class="appmarket-preview-image"></div>\n\
            <div class="d-flex justify-content-center"><label style="align-self: center;" title="Зураг сонгох" for="appmarket-file-upload" class="custom-file-upload">'+
                '<i class="icon-cloud-upload"></i>'+
            '</label><label style="align-self: center;" title="Зураг устгах" id="appmarket-delete-file-upload" class="custom-file-upload ml8">'+
                '<i class="icon-cross3"></i>'+
            '</label></div>'+
            '<input id="appmarket-file-upload" type="file"/></div>'
        );
        $dialog.dialog({
          cache: false,
          resizable: true,
          bgiframe: true,
          autoOpen: false,
          title: "Logo",
          width: 400,
          height: "auto",
          modal: true,
          open: function () {
          },
          close: function () {
            $dialog.empty().dialog("destroy").remove();
          },
          buttons: [
            {
